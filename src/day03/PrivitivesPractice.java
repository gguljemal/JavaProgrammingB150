package day03;

public class PrivitivesPractice {

    public static void main(String[] args) {

        byte letterCount = 26;

        System.out.println ("The letter acount is " + letterCount);

        short sheepCount = 300;

        System.out.println ("The number of sheep I have is " + sheepCount);

        int catCount = 20;

        System.out.println("my neighbor has a lot of cat, she has " + catCount);

        long mileToSun = 1000000l;

        System.out.println ("Mile to the sun is " + mileToSun);

        long mileToMoon = 500000L;

        System.out.println ("The mile to the Moon is " + mileToMoon);

       //------------------floating point-------------------

       float priceOfBanana = 1.99f; //must add f or F to show it is float data type!!!
       float priceOfPotato = 2.49F;

       System.out.println ("Banana price is " + priceOfBanana);

       System.out.println ("Price of the potato is " + priceOfPotato);

       //--------------larger floating point number----------------

        // program will run well without putting d or D to the end of number

        double priceOfIpad = 355.99d;

        System.out.println ("Ipads price is " + priceOfIpad);

        double priceOfIpadPro = 1024.99D;

        System.out.println ("Pro Ipad price is " + priceOfIpadPro);

        double priceOfMac = 2299.99;

        System.out.println ("Price of the Mac is " + priceOfMac);





    }
}
