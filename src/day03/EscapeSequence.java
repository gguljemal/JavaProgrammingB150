package day03;

public class EscapeSequence {

    public static void main(String[] args) {

        System.out.println ("Hello \\ world");
        System.out.println ("I like the book \"Java\" ");
        System.out.println ("Ahmet \tBahar");
        System.out.println ("Ahmet \n Bahar");
        System.out.println ("This book is Ahmets\' book");

        System.out.print ("Ahmet \n");
        System.out.println ("Bahar");




    }
}
