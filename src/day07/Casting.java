package day07;

public class Casting {

    public static void main(String[] args) {

        int num = 10;
        double bigNum = num;
        System.out.println(bigNum);

        int number = (int) 12.99;
        System.out.println(number);

        long miles = 10000;
        int mileTo= (int) miles;
        System.out.println(mileTo);







    }
}
