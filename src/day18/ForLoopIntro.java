package day18;

public class ForLoopIntro {

    public static void main(String[] args) {

        //count 1 to 5

        for (int i=1; i<=5; i++){

            System.out.println("i= "+i);
            System.out.println("HAPPY BIRTHDAY ROCSANA");
        }

        for (int x=10; x>=0; --x){

            System.out.println("x: "+x);
        }
    }
}
