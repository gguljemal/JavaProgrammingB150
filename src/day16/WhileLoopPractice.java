package day16;

public class WhileLoopPractice {

    public static void main(String[] args) {

        int x = 101;

        while (x<=105){
            System.out.println("Hello World!");
            System.out.println("x is "+x);
            ++x; }

        System.out.println("--------------------------------");

        int counter =0;

        while (counter<11){
            System.out.println("Counter values : "+counter);
            ++counter;
        }




    }
}
